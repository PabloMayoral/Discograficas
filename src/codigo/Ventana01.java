    private void closeConectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_closeConectionActionPerformed
        // este boton cierra la conexion con la base de datos
        if(gestor.cerrarConexion() == 0){
            mensaje.setText("desconexion realizada con exito");
            System.out.println("desconectado de la base de datos");
            openConection.setEnabled(true);
            closeConection.setEnabled(false);
        }
        else{
              System.out.println("no se ha podido desconectar de la base de datos");
        }
    }//GEN-LAST:event_closeConectionActionPerformed

