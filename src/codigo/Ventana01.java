       public void reseteaValoresBox() {
           //En este metodo el objetivo es añadir un item en blanco, que sera el
           //item inicial, dandole un toque estetico, a parte busca tambien implementar
           //dentro del comboBox todas las canciones o todos los album dependiendo 
           //del comboBox que se deba usar, despues se usa el remove item para 
           //una vez se añada, borre o modifique un album/cancion el cambio se muestre 
           //dentro del comboBox y sin que se repita.
        mostrarCancion_box.removeAllItems();
        Actualizar_box.removeAllItems();
        deleteBox.removeAllItems();
        seleccionaAlbumBox_cancion.removeAllItems();
        seleccionaAlbumBox_actualizar.removeAllItems();
        mostrarCancion_box.addItem("");
        Actualizar_box.addItem("");
        deleteBox.addItem("");
        seleccionaAlbumBox_cancion.addItem("");
        seleccionaAlbumBox_actualizar.addItem("");
        //estos bucles sirven para meter en los comboBox los albumes o las canciones 
        for (int i = 0; i < gestor.consulta_albumes().size(); i++) {
            mostrarCancion_box.addItem(gestor.consulta_albumes().get(i));
            seleccionaAlbumBox_cancion.addItem(gestor.consulta_albumes().get(i));
           seleccionaAlbumBox_actualizar.addItem(gestor.consulta_albumes().get(i));
        }
        for (int i = 0; i < gestor.consulta_Song().size(); i++) {
            Actualizar_box.addItem(gestor.consulta_Song().get(i));
            deleteBox.addItem(gestor.consulta_Song().get(i));
        }
    }
